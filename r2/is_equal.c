int	is_equal(char *s1, char *s2)
{
	int	res;
	int	index;

	index = 0;
	while (s1[index] != '\0' || s2[index] != '\0')
	{
		if (s1[index] == s2[index])
		index++;
		else
		{
			return (0);
		}
	}
	return (1);
}
